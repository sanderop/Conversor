package conversor;

public class Moneda {
	
	private String nombre;
	private Double valor;

	public Moneda() {
	}

	public String getNombre() {
		return nombre;
	}
	
	public String setNombre(String nombre) {
		return this.nombre = nombre;
	}

	public Double getValor() {
		return valor;
	}

	public void setValor(Double valor) {
		this.valor = valor;
	}
	
	
}
